    private void getdata() {
         DefaultTableModel model = (DefaultTableModel)tb_data.getModel();
         model.setRowCount(0);
         
         try {
             String sql ="Select * From biodata";
             PreparedStatement st = conn.prepareStatement(sql);
             ResultSet rs = st.executeQuery();
             
             while(rs.next()){
                 int id         = rs.getInt("id");
                 String nama    = rs.getString("nama");
                 String telpon  = rs.getString("telpon");
                 String alamat  = rs.getString("alamat");
                 
                 Object[] rowData = {id,nama,telpon,alamat};
                 model.addRow(rowData);
             }
             
             rs.close();
             st.close();
         } catch (Exception e) {
             Logger.getLogger(bagian_1.class.getName()).log(Level.SEVERE,null,e);
         }
    }
